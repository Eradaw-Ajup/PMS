package gov.nist.csd.pm.pep.response;

public class AccessToken {
	private int index;
	private String share;
	private String x_string;
	private String y_string;
	private String z_string;

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getShare() {
		return share;
	}

	public void setShare(String share) {
		this.share = share;
	}

	public String getX_string() {
		return x_string;
	}

	public void setX_string(String x_string) {
		this.x_string = x_string;
	}

	public String getY_string() {
		return y_string;
	}

	public void setY_string(String y_string) {
		this.y_string = y_string;
	}

	public String getZ_string() {
		return z_string;
	}

	public void setZ_string(String z_string) {
		this.z_string = z_string;
	}
}
